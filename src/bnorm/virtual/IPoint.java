package bnorm.virtual;

/**
 * Interface for a point in two dimensional space.
 *
 * @author Brian Norman
 * @version 1.0
 */
public interface IPoint {

   /**
    * Returns the <code>x</code> coordinate of the point.
    *
    * @return the <code>x</code> coordinate.
    */
   double getX();

   /**
    * Returns the <code>y</code> coordinate of the point.
    *
    * @return the <code>y</code> coordinate.
    */
   double getY();

}
